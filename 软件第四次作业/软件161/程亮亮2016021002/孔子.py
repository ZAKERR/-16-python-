#功能：输入年龄可以知孔子所做事

b=input("输入年龄:")
a=int(b)
if a<15:
    print("快乐的玩耍")
elif a<30:
    print("志于学习")
elif a<40:
    print("而立")
elif a<50:
    print("不惑")
elif a<60:
    print("知天命")
elif a<70:
    print("耳顺")
else:
    print("心所欲")
