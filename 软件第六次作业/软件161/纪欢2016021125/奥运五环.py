#奥运五环
import turtle
t=turtle.Pen()
t.color('blue')
t.penup()
t.goto(-110,-25)
t.pendown()
t.circle(45)

t.color('black')
t.penup()
t.goto(0,-25)
t.pendown()
t.circle(45)

t.color('red')
t.penup()
t.goto(110,-25)
t.pendown()
t.circle(45)

t.color('yellow')
t.penup()
t.goto(-55,-75)
t.pendown()
t.circle(45)

t.color('green')
t.penup()
t.goto(55,-75)
t.pendown()
t.circle(45)
t.done()
