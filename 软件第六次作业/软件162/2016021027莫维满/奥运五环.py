import turtle
t=turtle.Pen()
t.pensize(3)
t.color ("blue")
t.circle (30,360)
t.pu()
t.goto(60,0)
t.pd ()


t.color ("black")
t.circle (30,360)
t.pu()
t.goto(120,0)
t.pd ()


t.color ("red")
t.circle (30,360)
t.pu()
t.goto(90,-30)
t.pd ()


t.color ("green")
t.circle (30,360)
t.pu()
t.goto(30,-30)
t.pd ()


t.color ("yellow")
t.circle (30,360)
t.done()
