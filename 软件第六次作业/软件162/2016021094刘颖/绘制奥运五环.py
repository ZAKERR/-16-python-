#绘制奥运五环
import turtle
turtle.width(10)
turtle.color("blue")
turtle.circle(40)
turtle.penup()
turtle.goto(100,0)
turtle.pendown()
turtle.color("black")
turtle.circle(40)
turtle.penup()
turtle.goto(200,0)
turtle.pendown()
turtle.color("red")
turtle.circle(40)
turtle.penup()
turtle.goto(50,-50)
turtle.pendown()
turtle.color("yellow")
turtle.circle(40)
turtle.penup()
turtle.goto(150,-50)
turtle.pendown()
turtle.color("green")
turtle.circle(40)
